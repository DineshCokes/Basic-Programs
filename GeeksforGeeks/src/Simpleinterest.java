import java.util.Scanner;
public class Simpleinterest {
	public static void main(String args[])
	{
		int simpleinterest;
		Scanner in=new Scanner(System.in);
		int a=in.nextInt();
		int b=in.nextInt();
		int c=in.nextInt();
		simpleinterest=(a*b*c)/100;
		System.out.println(simpleinterest);
		
		
		
	}

}
